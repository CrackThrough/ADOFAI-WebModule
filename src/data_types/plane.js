/**
 * Enum for easy manage of plane on flash.
 */
const PLANE = {};

Object.defineProperty(PLANE, "FOREGROUND", {
  value: "Foreground",
  writable: false,
  enumerable: true,
  configurable: false,
});
Object.defineProperty(PLANE, "BACKGROUND", {
  value: "Background",
  writable: false,
  enumerable: true,
  configurable: false,
});

export default PLANE;
